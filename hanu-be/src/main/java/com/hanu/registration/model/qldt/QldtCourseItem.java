package com.hanu.registration.model.qldt;

public class QldtCourseItem {
    private String ma;
    private String ten;
    private String ten_eg;

    public QldtCourseItem() {
    }

    public String getMa() {
        return ma;
    }

    public void setMa(String ma) {
        this.ma = ma;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getTen_eg() {
        return ten_eg;
    }

    public void setTen_eg(String ten_eg) {
        this.ten_eg = ten_eg;
    }
}